const int Nr = 10;  // AES-128 
const int Nk = 4;   // 


int bytesToInteger(BYTE* buffer)     // buffer 4 bytes
{
    int a = int((BYTE)(buffer[0]) << 24 |
        (BYTE)(buffer[1]) << 16 |
        (BYTE)(buffer[2]) << 8 |
        (BYTE)(buffer[3]));
    return a;
}


BYTE* intToByte(int n) {
    BYTE byte[4];
    byte[0] = (n >> 24) & 0xFF;
    byte[1] = (n >> 16) & 0xFF;
    byte[2] = (n >> 8) & 0xFF;
    byte[3] = n & 0xFF;
    return byte;
}


